# a) Integer (int) ~> Bilangan bulat
x = 10
y = -5
print(type(x)) # Outputnya adalah <class 'int' >

# b) Floating Point (float) ~> Bilangan desimal
pi = 3.14
harga = 5000.75
print(type(pi)) # Outputnya adalah <class 'float' >

# c) String (str) ~> Teks atau karakter
     # Contoh string 1
nama = "Python"
print(type(nama)) #o Outputnya adalah <class 'str' >

    # Contoh string 2
teks = "Hello, World!"
print(teks.upper())  # Outputnya adalah HELLO, WORLD!
print(teks.lower())  # Outputnya adalah hello, world!
print(teks[0:5])     # Outputnya adalah Hello (Slicing)
print(teks.replace("World", "Python")) # Outputnya adalah Hello, Python!

# d) Boolean (Bool) ~> True or False
is_student = True
is_active = False
print(type(is_student)) # Outputnya adalah <class 'bool' >

# e) Complex (complex) ~> Bilangan kompleks
z = 3 + 4j
print(type(z)) # Outputnya adalah <class 'complex' >
print(z.real)  # Outputnya adalah 3.0
print(z.imag)  # Outputnya adalh 4.0 

