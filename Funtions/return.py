def add(x, y): # fungsinya adalah menerima 2 parameter
    print("x is %s, y is %s" %(x, y)) # menampilkan 2 parameter
        # %s adalah placeholder untuk string dalam string formatting lama
    return x + y # mengembalikan hasil penjumlahan 2 parameter
print(add(3, 6)) # menampilkan hasil penjumlahan 3 dan 6
