# Penulisan beberapa nilai ke beberapa variabel dalam 1 pernyataan 

# Contoh 1
x, y, z = "Orange", "Banana", "Cherry"
print(x)
print(y)
print(z)

# Contoh 2 
x = y = z = "Orange"
print(x)
print(y)
print(z)

# Contoh 3
fruits = ["apple", "banana", "cherry"]
x, y, z = fruits
print(x)
print(y)
print(z)